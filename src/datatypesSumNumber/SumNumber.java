package datatypesSumNumber;

public class SumNumber {
    public static void main(String[] args) {
        int a = 5;
        int myFirstNumber = 10;
        long myLongNumber = 5001;
        System.out.println(a);
        System.out.println(myFirstNumber);
        System.out.println(a + myLongNumber);
        System.out.println(myFirstNumber + myLongNumber);
    }
}
